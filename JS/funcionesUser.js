function esconderCampana1(){
    var divBell1 = document.getElementById("idPBell1");
    if (divBell1.style.display === "none") {
        divBell1.style.display = "block";
        divBell1.style.visibility = "visible";
    } else {
        divBell1.style.display = "none";
        divBell1.style.visibility = "hidden";
    }
}
